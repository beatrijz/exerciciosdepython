"""Crie um programa em Python que solicite ao usuário e receba os valores
da base e altura para calcular a área de um triangulo;"""
base = int (input ("Digite o tamanho da base do triângulo: "))
altura = int (input ("Digite o tamanho da altura do triângulo: "))
area = (base * altura)/2
print (f"O valor da área do triãngulo é: {area}")